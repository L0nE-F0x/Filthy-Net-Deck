/**
 * Regenerate pipeline/sources/mtgo-name-map.json — printed alias → canonical
 * Scryfall name, for sets whose printings carry a different printed_name
 * (Universes Beyond dual-identity cards). MTGO decklist JSON uses the printed
 * alias, Scryfall /cards/collection wants the canonical name.
 *
 * Every entry comes straight from Scryfall, so the map is verified by
 * construction — no hand-typed card names (no-fabrication rule).
 *
 * Usage: node scripts/gen-mtgo-name-map.mjs [setCode ...]   (default: om1)
 * Rerun when a new alias-printing set enters Standard/Pioneer — see
 * docs/MAINTENANCE.md.
 */
import { writeFileSync } from "node:fs";
import { BOT, UA } from "../pipeline/sources/common.mjs";

const sets = process.argv.slice(2).length ? process.argv.slice(2) : ["om1"];
const map = {};

for (const set of sets) {
  let url = `https://api.scryfall.com/cards/search?q=${encodeURIComponent(`set:${set}`)}&unique=prints`;
  let pages = 0;
  while (url) {
    const res = await fetch(url, {
      headers: { Accept: "application/json", "User-Agent": UA, From: BOT },
    });
    if (!res.ok) {
      console.error(`ABORT: ${set} → HTTP ${res.status}. Map not written.`);
      process.exit(1);
    }
    const body = await res.json();
    pages++;
    for (const card of body.data ?? []) {
      const printed = card.printed_name?.trim();
      const canonical = card.name?.trim();
      if (!printed || !canonical || printed === canonical) continue;
      const prev = map[printed];
      if (prev && prev !== canonical) {
        console.error(
          `ABORT: alias "${printed}" maps to both "${prev}" and "${canonical}" — ambiguous, refusing to write.`,
        );
        process.exit(1);
      }
      map[printed] = canonical;
    }
    url = body.has_more ? body.next_page : null;
    await new Promise((r) => setTimeout(r, 120));
  }
  console.log(`  ${set}: scanned ${pages} page(s)`);
}

const out = {
  _comment:
    "GENERATED by scripts/gen-mtgo-name-map.mjs — printed UB alias → canonical Scryfall name. Do not hand-edit; rerun the script (see docs/MAINTENANCE.md).",
  _generatedAt: new Date().toISOString().slice(0, 10),
  _sets: sets,
  names: Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b))),
};
writeFileSync(
  "pipeline/sources/mtgo-name-map.json",
  JSON.stringify(out, null, 2) + "\n",
);
console.log(`Wrote ${Object.keys(map).length} alias mappings.`);
